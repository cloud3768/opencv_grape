class StarBottleneck(nn.Module):
    # Ghost Bottleneck https://github.com/huawei-noah/ghostnet
    def __init__(self, c1, c2, k=3, s=1):  # ch_in, ch_out, kernel, stride
        super().__init__()
        c_ = c2 // 2
        self.conv = nn.Sequential(
            GhostConv(c1, c_, 1, 1),  # DFC Attention
            DWConv(c_, c_, 7, 1, 6, act=True),  # dw
            GhostConv(c_, c1, 1, 1, act=False))  # pw-linear
        self.dwconv = nn.Sequential(Conv(c1, c1, 1, 1, act=True),
                                      DWConv(c1, c2, 7, 1, 6, act=False))
        self.act = nn.ReLU6()

    def forward(self, x):
        return self.dwconv(self.conv(x) * self.act(self.conv(x)))


